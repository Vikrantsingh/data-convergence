package etl;

public class DatasetUploaderModel {
	private String department;
	private String datasetName;
	private String country;
	private String countryColName;
	private String state;
	private String stateColName;
	private String district;
	private String districtColName;
	private String fileFormat;
	private String fileName;
	 
	
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getDatasetName() {
		return datasetName;
	}
	public void setDatasetName(String datasetName) {
		this.datasetName = datasetName;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getCountryColName() {
		return countryColName;
	}
	public void setCountryColName(String countryColName) {
		this.countryColName = countryColName;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getStateColName() {
		return stateColName;
	}
	public void setStateColName(String stateColName) {
		this.stateColName = stateColName;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getDistrictColName() {
		return districtColName;
	}
	public void setDistrictColName(String districtColName) {
		this.districtColName = districtColName;
	}
	public String getFileFormat() {
		return fileFormat;
	}
	public void setFileFormat(String fileFormat) {
		this.fileFormat = fileFormat;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	
	
 

}
